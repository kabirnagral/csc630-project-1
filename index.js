/*
  Project 1
  Simple REST API to create users and addresses
*/

var pg = require("pg");
var express = require("express");
var bodyParser = require('body-parser');
var NodeGeocoder = require("node-geocoder");

var geocoder = NodeGeocoder({
  provider: 'google',
  apiKey: process.env.GOOGLE_API,
});

var pool = new pg.Pool(),
    app = express();

app.use(bodyParser());

/*
  CREATE TABLES
*/

pool.query('CREATE TABLE Users (UserID int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, DisplayName varchar(255), UserName varchar(255), Lat float(5), Long float(5))', function(err, res){
  console.log(err, res);
});

pool.query('CREATE TABLE Addresses (AddressID int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, AddressTitle varchar(255),Address varchar(255), Lat float(5), Long float(5), UserID int)', function(err, res){
  console.log(err, res);
});

/*
  ADDRESSES
*/

// Create address
app.post("/address/create", function(req, res){
  geocoder.geocode(req.body.address, function(err, res) {
    pool.query(`INSERT INTO Addresses (AddressTitle,Address,Lat,Long,UserID) VALUES ('${req.body.addressTitle}','${req.body.address}',${res[0].latitude},${res[0].longitude},${req.body.userID});`, function(err, res){
      console.log("Created Address");
      res.send(200);
    });
  });
});

//Update address
app.post("/address/update", function(req, res){
  geocoder.geocode(req.body.address, function(err, res) {
    pool.query(
      `UPDATE Addresses
      SET AddressTitle = '${req.body.addressTitle}', Address = '${req.body.address}', Lat = ${res[0].latitude}, Long = ${res[0].longitude}
      WHERE AddressID = '${req.body.addressID}';`, function(err, res){
      console.log("Updated Address");
      res.send(200);
    });
  });
});

//Delete address
app.post("/address/delete", function(req, res){
  pool.query(`DELETE FROM Addresses WHERE AddressID=${req.body.addressID}`, function(err, result){
    console.log("Deleted Address");
    res.send(200);
  });
});

// Returns a JSON list of all the userâ€™s locations.
app.get("/:username/poi", function(req, res){
  pool.query(`SELECT AddressTitle,Address,Lat,Long FROM Users JOIN Addresses ON Users.UserID=Addresses.UserID WHERE Users.UserName='${req.params.username}'`, function(err, result){
    res.json(result.rows);
  });
});

/*
  USERS
*/


// Creates a User
app.post("/user/create", function(req, res){
  pool.query(`INSERT INTO Users (DisplayName,UserName,Lat,Long) VALUES ('${req.body.displayName}','${req.body.userName}',${req.body.lat},${req.body.long});`, function(err, res){
    console.log("Created USER");
    res.send(200);
  });
});

// Updates a User
app.post("/user/update", function(req, res){
  pool.query(
    `UPDATE Users
    SET DisplayName = '${req.body.displayName}', UserName = '${req.body.userName}', Lat = ${req.body.lat}, Long = ${req.body.long}
    WHERE UserID = '${req.body.userID}';`, function(err, res){
    console.log("Updated USER");
    res.send(200);
  });
});

// Deletes a User
app.post("/user/delete", function(req, res){
  pool.query(
    `DELETE FROM Users
    WHERE UserID = '${req.body.userID}';`, function(err, res){
    console.log("Deleted USER");
    res.send(200);
  });
});

// Gets a User
app.get("/:username", function(req, res){
  pool.query(`SELECT * FROM Users WHERE Users.UserName='${req.params.username}'`, function(err, result){
    res.json(result.rows);
  });
});

app.listen(process.env.PORT || 3000, function(){
  console.log("Listening on " + (process.env.PORT || 3000));
});
